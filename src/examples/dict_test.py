# Test dictionary literals

# Simple empty dict
empty_dict = {}

# Simple dict with string keys
person = {"name": "Alice", "age": "30", "city": "NYC"}

# Dict with various value types  
mixed_dict = {"key1": 42, "key2": "string", "key3": 3.14}

# Nested dict
nested = {"outer": {"inner": "value"}, "count": 5}

# Dict with numeric keys
numeric_keys = {1: "one", 2: "two", 3: "three"}

# Inline dict
inline = {"x": 10, "y": 20}

# Print dicts
print(person)
print(empty_dict)
print(mixed_dict)
print(nested)
print(numeric_keys)
print(inline)
